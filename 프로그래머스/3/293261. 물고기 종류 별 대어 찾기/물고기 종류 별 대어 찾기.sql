WITH BIG AS(
    SELECT
        FISH_TYPE, MAX(LENGTH) MAX_LENGTH
    FROM
        FISH_INFO 
    GROUP BY
        FISH_TYPE
)


SELECT
    ID, FISH_NAME, LENGTH
FROM
    FISH_INFO 
JOIN 
    FISH_NAME_INFO USING(FISH_TYPE)
JOIN
    BIG USING(FISH_TYPE)
WHERE
    LENGTH = MAX_LENGTH
ORDER BY
    ID ASC;